# Solicitarle el nombre completo al usuario
nombre = input("Ingresa tu nombre completo: ")

# Mostrar el nombre en mayúsculas
print("nombre en mayúsculas:", nombre.upper())

# Mostrar el nombre en minúsculas
print("nombre en minúsculas:", nombre.lower())

# Cantidad de caracteres que contiene el nombre y incluye también los espacio
print("cantidad de caracteres:", len(nombre))
